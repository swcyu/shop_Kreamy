package com.mvc.dto;

public class WishProductDTO {

	private int prodNum;
	private String engProduct;
	private String korProduct;
	private String userSize;
	private String imageName;
	
	private int wishCheckNum;
	
	
	
	
	public int getWishCheckNum() {
		return wishCheckNum;
	}
	public void setWishCheckNum(int wishCheckNum) {
		this.wishCheckNum = wishCheckNum;
	}
	public int getProdNum() {
		return prodNum;
	}
	public void setProdNum(int prodNum) {
		this.prodNum = prodNum;
	}
	public String getEngProduct() {
		return engProduct;
	}
	public void setEngProduct(String engProduct) {
		this.engProduct = engProduct;
	}
	public String getKorProduct() {
		return korProduct;
	}
	public void setKorProduct(String korProduct) {
		this.korProduct = korProduct;
	}
	public String getUserSize() {
		return userSize;
	}
	public void setUserSize(String userSize) {
		this.userSize = userSize;
	}
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	
	
	
	
	
	
}
